package Day2;

public class DecisionMakingWithOperators {

	public static void main(String[] args) {
		int x = 27, y = 72;
		int a = 7;
		int b = 9;

		if (x >= y) {
			System.out.println("true");
		} else {
			System.out.println("false");
		}

		if ((a < b) || (a == b)) {
			System.out.println("Condition is TRUE");
		} else

		{
			System.out.println("Condition is FALSE");
		}

	}

}
