package Day2;

public class ForLoopExample {

	public static void main(String[] args) {
		
		for(int i = 1; i<=10 ; i++)   //(int i = 1000; i<=1;i--)
		{	
			System.out.print("Value of i:");
			System.out.println(i);
		}	
		System.out.println();

		for(int i = 100; i>=90;i--)
		{	
			System.out.print("Value of i:");
			System.out.println(i);
		}
	}

}
