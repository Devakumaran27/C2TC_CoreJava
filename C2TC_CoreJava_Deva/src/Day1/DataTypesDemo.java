package Day1;

public class DataTypesDemo {

	public static void main(String[] args) {
		

		int value1 = 27 / 3; 
		float value2 = 729 / 61;
		double value3 = 50d / 4d;
		float value4 = 78/8.6f; 
		System.out.println("value 1 (Int) = " + value1); 
		System.out.println("value 2 (Float) = " + value2);
		System.out.println("value 3 (Double) = "  + value3);
		System.out.println("value 4 (Float) = " + value4);
		
		
		int mark = 467;
		double percentage = mark * 0.46f;
		System.out.println("percentage : " + percentage);
		
	}

}
